package pojos;

/* Information needed for the 1st Step of Registration */

public class RegistrationDataStep1 {
    String username;
    String password;
    String email;
    String id;

    public RegistrationDataStep1() {

    }

    public RegistrationDataStep1(String username, String password, String email) {
        this.username = username;
        this.password = password;
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
